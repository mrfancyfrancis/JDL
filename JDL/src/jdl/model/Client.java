package jdl.model;

import java.util.Date;

public class Client 
{
	private int client_id;
	private String client_lastname;
	private String client_firstname;
	private String client_nationality;
	private Date client_birthdate;
	private String client_gender;
	private String client_company;
	private String client_position;
	private String client_alias;
	private String client_contact;
	private String client_email;
	public int getClient_id() {
		return client_id;
	}
	public void setClient_id(int client_id) {
		this.client_id = client_id;
	}
	public String getClient_lastname() {
		return client_lastname;
	}
	public void setClient_lastname(String client_lastname) {
		this.client_lastname = client_lastname;
	}
	public String getClient_firstname() {
		return client_firstname;
	}
	public void setClient_firstname(String client_firstname) {
		this.client_firstname = client_firstname;
	}
	public String getClient_nationality() {
		return client_nationality;
	}
	public void setClient_nationality(String client_nationality) {
		this.client_nationality = client_nationality;
	}
	public Date getClient_birthdate() {
		return client_birthdate;
	}
	public void setClient_birthdate(Date client_birthdate) {
		this.client_birthdate = client_birthdate;
	}
	public String getClient_gender() {
		return client_gender;
	}
	public void setClient_gender(String client_gender) {
		this.client_gender = client_gender;
	}
	public String getClient_company() {
		return client_company;
	}
	public void setClient_company(String client_company) {
		this.client_company = client_company;
	}
	public String getClient_position() {
		return client_position;
	}
	public void setClient_position(String client_position) {
		this.client_position = client_position;
	}
	public String getClient_alias() {
		return client_alias;
	}
	public void setClient_alias(String client_alias) {
		this.client_alias = client_alias;
	}
	public String getClient_contact() {
		return client_contact;
	}
	public void setClient_contact(String client_contact) {
		this.client_contact = client_contact;
	}
	public String getClient_email() {
		return client_email;
	}
	public void setClient_email(String client_email) {
		this.client_email = client_email;
	} 
}
